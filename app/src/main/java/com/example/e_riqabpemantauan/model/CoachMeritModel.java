package com.example.e_riqabpemantauan.model;

public class CoachMeritModel {

    private int id;
    private String name;
    private int totalMerit;

    public CoachMeritModel() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTotalMerit() {
        return totalMerit;
    }

    public void setTotalMerit(int totalMerit) {
        this.totalMerit = totalMerit;
    }
}
